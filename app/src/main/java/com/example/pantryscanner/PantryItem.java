package com.example.pantryscanner;

public class PantryItem {
    private String itemName, itemType, expirationDate;

    public String getItemName() {return itemName;}
    public void setItemName(String item) {this.itemName = item;}
    public String getItemType() {return itemType;}
    public void setItemType(String item) {this.itemType = item;}
    public String getExpirationDate() {return expirationDate;}
    public void setExpirationDate(String expiration) {this.expirationDate = expiration;}

}
